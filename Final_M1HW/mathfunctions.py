# This module has functions that calculate the math equations

# function to calculate correct answer to ADD equation
def calc_add(num1, num2):
  """
  Function adds 2 numbers that the user inputs and returns total
  """
  total = num1 + num2

  return total

# function to calculate correct answer to SUBTRACT equation
def calc_subtract(num1, num2):
  """
  Function substracts 2 numbers that the user inputs and returns total
  """
  total = num1 - num2

  return total

# function to calculate correct answer to DIVIDE equation
def calc_divide(num1, num2):
  """
  Function divides 2 numbers that the user inputs and returns total
  """
  quotient = num1 / num2

  return quotient

# function to calculate correct answer to MULTIPLY equation
def calc_multiply(num1, num2):
  """
  Function multiplies 2 numbers that the user inputs and returns total
  """
  product = num1 * num2

  return product

